package clase;

public class Problema4 {

    private int h;
    private int j;
    private int k;
    private int l;

    public Problema4(int h, int j, int k, int l) {

        this.j = k;
        this.k = h;
        this.h = l;
        this.l = j;

    }

    public int getH() {
        return h;
    }

    public int getJ() {
        return j;
    }

    public int getK() {
        return k;
    }

    public int getL() {
        return l;
    }

}
