package clase;

public class Problema2 {

    public Problema2() {
    }

    public double Suma(int X, int Y) {

        double suma = X + Y;

        return suma;

    }

    public double Resta(double n, double m) {

        double resta = n - m;

        return resta;
    }

    public double Multiplicación(int X, double m) {

        double multi = X * m;

        return multi;
    }

    public double División(double n, int Y) {

        double divi = n / Y;

        return divi;
    }

}
